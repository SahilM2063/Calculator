let emp_string = "";
let buttons = document.querySelectorAll(".btn");
Array.from(buttons).forEach((btn) => {
    btn.addEventListener('click', (e) => {
        if (e.target.innerHTML == '=') {
            emp_string = eval(emp_string);
            document.querySelector('#screen').value = emp_string;
        }
        else if (e.target.innerHTML == 'AC') {
            emp_string = "";
            document.querySelector('#screen').value = emp_string;
        }
        else if (e.target.innerHTML == 'CHK') {
            emp_string = emp_string.slice(0, -1);
            document.querySelector('#screen').value = emp_string;
        }
        else if (e.target.innerHTML == 'X') {
            emp_string = emp_string += "*";
            document.querySelector('#screen').value = emp_string;
        }
        else if (e.target.innerHTML == 'รท') {
            emp_string = emp_string += "/";
            document.querySelector('#screen').value = emp_string;
        }
        else {
            console.log(e.target)
            emp_string = emp_string + e.target.innerHTML;
            document.querySelector('#screen').value = emp_string;
        }
    })
})


